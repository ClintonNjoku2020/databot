def build_powerpoint(title, brief, points):
    title = clean_title(title)
    points = parse_points("\n".join(points))
    slides = [
        (title, [brief or "Professional artifact generated by DataBot.", "Prepared for review and decision-making."]),
        ("Key Findings", points[:5]),
        ("Recommended Next Steps", [
            "Validate assumptions and data sources.",
            "Prioritize the highest-impact action.",
            "Define owners, deadlines, and success metrics.",
        ]),
    ]
    output = io.BytesIO()
    with zipfile.ZipFile(output, "w", zipfile.ZIP_DEFLATED) as pptx:
        pptx.writestr("[Content_Types].xml", _content_types(len(slides)))
        pptx.writestr("_rels/.rels", _root_rels())
        pptx.writestr("ppt/presentation.xml", _presentation_xml(len(slides)))
        pptx.writestr("ppt/_rels/presentation.xml.rels", _presentation_rels(len(slides)))
        for index, (slide_title, bullets) in enumerate(slides, 1):
            pptx.writestr(f"ppt/slides/slide{index}.xml", _slide_xml(slide_title, bullets))
            pptx.writestr(f"ppt/slides/_rels/slide{index}.xml.rels", '<?xml version="1.0" encoding="UTF-8" standalone="yes"?><Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships"/>')
    return output.getvalue()


def _content_types(slide_count):
    overrides = "\n".join(
        f'<Override PartName="/ppt/slides/slide{index}.xml" ContentType="application/vnd.openxmlformats-officedocument.presentationml.slide+xml"/>'
        for index in range(1, slide_count + 1)
    )
    return f'''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">
  <Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>
  <Default Extension="xml" ContentType="application/xml"/>
  <Override PartName="/ppt/presentation.xml" ContentType="application/vnd.openxmlformats-officedocument.presentationml.presentation.main+xml"/>
  {overrides}
</Types>'''


def _root_rels():
    return '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  <Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="ppt/presentation.xml"/>
</Relationships>'''


def _presentation_rels(slide_count):
    rels = "\n".join(
        f'<Relationship Id="rId{index}" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/slide" Target="slides/slide{index}.xml"/>'
        for index in range(1, slide_count + 1)
    )
    return f'''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  {rels}
</Relationships>'''


def _presentation_xml(slide_count):
    slide_ids = "\n".join(
        f'<p:sldId id="{255 + index}" r:id="rId{index}"/>'
        for index in range(1, slide_count + 1)
    )
    return f'''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<p:presentation xmlns:a="http://schemas.openxmlformats.org/drawingml/2006/main" xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships" xmlns:p="http://schemas.openxmlformats.org/presentationml/2006/main">
  <p:sldIdLst>{slide_ids}</p:sldIdLst>
  <p:sldSz cx="9144000" cy="5143500" type="screen16x9"/>
  <p:notesSz cx="6858000" cy="9144000"/>
</p:presentation>'''


def safe_filename(title, suffix):
    stem = "".join(char.lower() if char.isalnum() else "-" for char in clean_title(title))
    stem = "-".join(part for part in stem.split("-") if part)[:64] or "databot-artifact"
    return f"{stem}.{suffix.lstrip('.')}"


def generate_artifacts(title, brief, points_text, csv_file_bytes=None):
    points = parse_points(points_text)
    columns, rows = parse_csv_bytes(csv_file_bytes)
    labels, values = infer_chart_data(columns, rows)
    chart_title = f"{clean_title(title)} Chart"
    diagram_title = f"{clean_title(title)} Workflow"
    return {
        "pdf": build_pdf_report(title, brief, points),
        "chart_svg": build_chart_svg(chart_title, labels, values),
        "diagram_svg": build_diagram_svg(diagram_title, points),
        "pptx": build_powerpoint(title, brief, points),
    }
